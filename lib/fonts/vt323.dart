import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

TextStyle textStyleVt323(
    {double? size,
    Color? color,
    FontWeight? fontWeight,
    double? letterSpacing}) {
  return GoogleFonts.vt323(
    fontSize: size,
    color: color,
    fontWeight: fontWeight,
    letterSpacing: letterSpacing,
  );
}
